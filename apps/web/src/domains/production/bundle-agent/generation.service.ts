import crypto from "crypto";
import JSZip from "jszip";
import type { BundleAgentSpec } from "./types";

function escapeString(value: string): string {
  return JSON.stringify(value);
}

function buildTemplateSource(spec: BundleAgentSpec): string {
  return `import React from "react";
import { AbsoluteFill, interpolate, useCurrentFrame, useVideoConfig } from "remotion";

type TemplateProps = {
  title?: string;
  subtitle?: string;
};

const stylePreset = ${escapeString(spec.visualStyle)};

export default function SofliaGeneratedTemplate(props: TemplateProps) {
  const frame = useCurrentFrame();
  const { durationInFrames } = useVideoConfig();
  const progress = interpolate(frame, [0, Math.max(1, durationInFrames - 1)], [0, 1], {
    extrapolateLeft: "clamp",
    extrapolateRight: "clamp",
  });
  const title = props.title || ${escapeString(spec.title)};
  const subtitle = props.subtitle || ${escapeString(spec.description || "Courseforge visual lesson")};

  return (
    <AbsoluteFill
      style={{
        background: "linear-gradient(135deg, #0f172a 0%, #1d4ed8 52%, #0f766e 100%)",
        color: "white",
        fontFamily: "Inter, Arial, sans-serif",
        justifyContent: "center",
        padding: 96,
      }}
    >
      <div style={{ maxWidth: 1180 }}>
        <div
          style={{
            display: "inline-flex",
            border: "1px solid rgba(255,255,255,0.34)",
            borderRadius: 999,
            padding: "10px 18px",
            fontSize: 26,
            marginBottom: 36,
            background: "rgba(15,23,42,0.42)",
          }}
        >
          SofLIA Bundle Agent
        </div>
        <h1
          style={{
            fontSize: 96,
            lineHeight: 1,
            margin: 0,
            letterSpacing: 0,
            transform: \`translateY(\${(1 - progress) * 18}px)\`,
            opacity: interpolate(frame, [0, 24], [0, 1], { extrapolateRight: "clamp" }),
          }}
        >
          {title}
        </h1>
        <p
          style={{
            fontSize: 38,
            lineHeight: 1.3,
            maxWidth: 920,
            marginTop: 32,
            color: "rgba(255,255,255,0.86)",
          }}
        >
          {subtitle}
        </p>
        <p
          style={{
            fontSize: 24,
            lineHeight: 1.45,
            maxWidth: 980,
            marginTop: 36,
            color: "rgba(255,255,255,0.7)",
          }}
        >
          Visual direction: {stylePreset}
        </p>
        <div
          style={{
            height: 8,
            width: 680,
            background: "rgba(255,255,255,0.18)",
            borderRadius: 999,
            overflow: "hidden",
            marginTop: 54,
          }}
        >
          <div
            style={{
              height: "100%",
              width: \`\${Math.round(progress * 100)}%\`,
              background: "white",
            }}
          />
        </div>
      </div>
    </AbsoluteFill>
  );
}
`;
}

function buildManifest(spec: BundleAgentSpec) {
  return {
    name: spec.title,
    entryPoint: "src/index.tsx",
    compositionId: spec.compositionId,
    exportMode: "component",
    defaultDurationFrames: spec.durationFrames,
    fps: spec.fps,
    width: spec.width,
    height: spec.height,
    propsSchema: spec.propsSchema,
    defaultProps: spec.defaultProps,
    remotionVersion: "4.0.484",
  };
}

export async function buildControlledBundleZip(spec: BundleAgentSpec): Promise<{
  buffer: ArrayBuffer;
  hash: string;
  originalFileName: string;
}> {
  const zip = new JSZip();
  zip.file("courseforge-remotion-template.json", JSON.stringify(buildManifest(spec), null, 2));
  zip.file("src/index.tsx", buildTemplateSource(spec));
  zip.file("package.json", JSON.stringify({
    private: true,
    dependencies: {
      react: "19.2.3",
      "react-dom": "19.2.3",
      remotion: "4.0.484",
    },
  }, null, 2));
  zip.file("README.md", `# ${spec.title}\n\nGenerated by SofLIA Bundle Agent as a non-approved draft bundle.\n`);

  const buffer = await zip.generateAsync({ type: "arraybuffer" });
  const hash = crypto.createHash("sha256").update(Buffer.from(buffer)).digest("hex");

  return {
    buffer,
    hash,
    originalFileName: `${spec.compositionId}.zip`,
  };
}
